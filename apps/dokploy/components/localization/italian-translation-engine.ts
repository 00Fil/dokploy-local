function sentenceCase(value: string) {
  return value.charAt(0).toUpperCase() + value.slice(1);
}

function definiteArticle(entity: { singular: string; gender: "m" | "f" }) {
  if (/^[aeiouàèéìòù]/i.test(entity.singular)) return "l’";
  return entity.gender === "f" ? "la " : "il ";
}

function translateTemplate(value: string): string | undefined {
  let match = value.match(/^([A-Za-z]+) (created|deleted|updated|saved|added|removed|started|stopped|reloaded|rebuilt|deployed|configured) successfully[.!]?$/i);
  if (match) {
    const entity = entities[match[1]!.toLowerCase()];
    const operation = operationPast[match[2]!.toLowerCase()];
    if (entity && operation)
      return `${sentenceCase(entity.singular)} ${operation[entity.gender]} correttamente`;
  }

  match = value.match(/^Failed to (create|delete|update|save|add|remove) (?:the )?([A-Za-z]+)[.!]?$/i);
  if (match) {
    const verbs: Record<string, string> = {
      create: "creare",
      delete: "eliminare",
      update: "aggiornare",
      save: "salvare",
      add: "aggiungere",
      remove: "rimuovere",
    };
    const entity = entities[match[2]!.toLowerCase()];
    if (entity)
      return `Impossibile ${verbs[match[1]!.toLowerCase()]} ${definiteArticle(entity)}${entity.singular}`;
  }

  match = value.match(/^No ([A-Za-z]+) found[.!]?$/i);
  if (match) {
    const raw = match[1]!.toLowerCase();
    const singular = raw.endsWith("s") ? raw.slice(0, -1) : raw;
    const entity = entities[singular];
    if (entity) return `Nessun${entity.gender === "f" ? "a" : ""} ${entity.singular} trovat${entity.gender === "f" ? "a" : "o"}`;
  }

  match = value.match(/^Select (?:a|an|the) ([A-Za-z]+)[.!]?$/i);
  if (match) {
    const entity = entities[match[1]!.toLowerCase()];
    if (entity) return `Seleziona ${entity.gender === "f" ? "una" : "un"} ${entity.singular}`;
  }

  match = value.match(/^Delete ([A-Za-z]+)[.!]?$/i);
  if (match) {
    const entity = entities[match[1]!.toLowerCase()];
    if (entity) return `Elimina ${entity.gender === "f" ? "la" : "il"} ${entity.singular}`;
  }

  match = value.match(/^Error ([a-z]+) (?:the |this |a |an )?(.+?)[.!]?$/i);
  if (match) {
    const action = errorActions[match[1]!.toLowerCase()];
    const rawObject = match[2]!.toLowerCase();
    const object = errorObjects[rawObject];
    if (action && object) return `Errore durante ${action} ${object}`;
  }

  match = value.match(/^(\d+) (minute|minutes|hour|hours|day|days|year|years|line|lines)$/i);
  if (match) {
    const units: Record<string, string> = {
      minute: "minuto",
      minutes: "minuti",
      hour: "ora",
      hours: "ore",
      day: "giorno",
      days: "giorni",
      year: "anno",
      years: "anni",
      line: "riga",
      lines: "righe",
    };
    return `${match[1]} ${units[match[2]!.toLowerCase()]}`;
  }

  return undefined;
}

function preserveWhitespace(source: string, translated: string) {
  const leading = source.match(/^\s*/)?.[0] ?? "";
  const trailing = source.match(/\s*$/)?.[0] ?? "";
  return `${leading}${translated}${trailing}`;
}

export function translateItalian(
  value: string,
  context: TranslationContext = "generic",
): string {
  const core = value.trim();
  if (!core || core.length > 500 || /[{}<>`]|https?:\/\//.test(core)) return value;

  const translated = contextual[context]?.[core] ?? exact[core] ?? translateTemplate(core);
  return translated ? preserveWhitespace(value, translated) : value;
}

